const mongoose = require("mongoose");

const retailerSchema = new mongoose.Schema({
  retailerId: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Retailer =
  mongoose.models.Retailer || mongoose.model("Retailer", retailerSchema);

module.exports = Retailer;
